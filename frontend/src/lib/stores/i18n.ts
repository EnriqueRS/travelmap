export type TranslationKey = string; // E.g., "nav.trips"

export const t = derived(languageStore, ($lang) => {
  return (key: TranslationKey, variables?: Record<string, string | number>) => {
    const keys = key.split(".");
    let value: any = translations[$lang];

    for (const k of keys) {
      if (value === undefined) break;
      value = value[k];
    }

    if (value === undefined) {
      // Fallback to Spanish if key missing
      value = translations["es"];
      for (const k of keys) {
        if (value === undefined) break;
        value = value[k];
      }
    }

    if (typeof value === "string" && variables) {
      return value.replace(/\{(\w+)\}/g, (_, match) => String(variables[match] || ""));
    }

    return value || key;
  };
});
